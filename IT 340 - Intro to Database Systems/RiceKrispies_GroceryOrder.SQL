CREATE TABLE SUPPLIER(
	SUPPLIER_ID NUMBER(10)				NOT NULL PRIMARY KEY,
	SUPPLIER_NAME VARCHAR(30)			DEFAULT NULL);

CREATE TABLE INGREDIENT(
	INGREDIENT_ID NUMBER(10)			NOT NULL PRIMARY KEY,
	INGREDIENT_NAME VARCHAR(50)			DEFAULT NULL);
	
CREATE TABLE BOX(
	BOX_ID NUMBER(10)				NOT NULL PRIMARY KEY,
	BOX_SIZE VARCHAR(30)				DEFAULT NULL,
	BOX_NAME VARCHAR(30)				DEFAULT NULL);
	
CREATE TABLE CEREAL(
	CEREAL_ID NUMBER(10)				NOT NULL PRIMARY KEY,
	CEREAL_NAME VARCHAR(30)				DEFAULT NULL,
	CEREAL_SERVING_SIZE VARCHAR(30)			DEFAULT NULL);

CREATE TABLE NUTRIENT(
	NUTRIENT_ID NUMBER(10)				NOT NULL PRIMARY KEY,
	NUTRIENT_RCD_DAILY_ALL VARCHAR(20)		DEFAULT NULL,
	NUTRIENT_SCIENTIFIC_NAME VARCHAR(40)		DEFAULT NULL,
	NUTRIENT_CONTENT_UNIT VARCHAR(5)		DEFAULT NULL);

CREATE TABLE RECIPE(
	INGREDIENT_ID NUMBER(10)			NOT NULL,
	CEREAL_ID NUMBER(10)				NOT NULL,
	PRIMARY KEY(INGREDIENT_ID,CEREAL_ID),
	CI_INGREDIENT_AMOUNT NUMBER(10)			DEFAULT(0),
	CI_UNIT_OF_MEASURE VARCHAR(5)			DEFAULT NULL,
	CONSTRAINT R_INGREDIENT_ID_FK FOREIGN KEY(INGREDIENT_ID) REFERENCES INGREDIENT(INGREDIENT_ID),
	CONSTRAINT R_CEREAL_ID_FK FOREIGN KEY(CEREAL_ID) REFERENCES CEREAL(CEREAL_ID));
	
CREATE TABLE SUPPLIER_INGREDIENT(
	INGREDIENT_ID NUMBER(10)			NOT NULL,
	SUPPLIER_ID NUMBER(10)				NOT NULL,
	PRIMARY KEY(INGREDIENT_ID,SUPPLIER_ID),
	SI_PRICE NUMBER(4,2)				DEFAULT(0.00),
	CONSTRAINT SI_INGREDIENT_ID_FK FOREIGN KEY (INGREDIENT_ID) REFERENCES INGREDIENT(INGREDIENT_ID),
	CONSTRAINT SI_SUPPLIER_ID_FK FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER(SUPPLIER_ID));
	
CREATE TABLE CEREAL_BOX(
	CEREAL_PACK_ID NUMBER(10)			NOT NULL PRIMARY KEY,
	CEREAL_ID NUMBER(10)				NOT NULL,
	BOX_ID NUMBER(10)				NOT NULL,
	MANUFACTURER_PRICE NUMBER(4,2)			DEFAULT(0.00),
	SUGGESTED_RETAIL_PRICE NUMBER(4,2)		DEFAULT(0.00),
	CONSTRAINT CB_CEREAL_ID_FK FOREIGN KEY (CEREAL_ID) REFERENCES CEREAL(CEREAL_ID),
	CONSTRAINT CB_BOX_ID_FK FOREIGN KEY (BOX_ID) REFERENCES BOX(BOX_ID));
	
CREATE TABLE INGREDIENT_NUTRIENT(
	INGREDIENT_NUTRIENT_ID NUMBER(10)		NOT NULL PRIMARY KEY,
	NUTRIENT_ID NUMBER(10)				      NOT NULL,
	INGREDIENT_ID NUMBER(10)			      NOT NULL,
	IN_NUTRIENT_AMOUNT NUMBER(10)				      DEFAULT(0),
	IN_UNIT_OF_MEASURE VARCHAR(10)			DEFAULT NULL,
	CONSTRAINT IN_INGREDIENT_ID_FK FOREIGN KEY(INGREDIENT_ID) REFERENCES INGREDIENT(INGREDIENT_ID),
	CONSTRAINT IN_NUTRIENT_ID_FK FOREIGN KEY(NUTRIENT_ID) REFERENCES NUTRIENT(NUTRIENT_ID));
	
CREATE TABLE GROCERY_STORE (
	GROCERYSTORE_ID NUMBER(10)		NOT NULL PRIMARY KEY,
	GSTORE_NAME VARCHAR(25)			NOT NULL,
	GSTORE_STREET VARCHAR(35)		DEFAULT NULL,
	GSTORE_CITY VARCHAR(20)			DEFAULT NULL,
	GSTORE_STATE VARCHAR(2)			DEFAULT 'MN');

CREATE TABLE PRODUCT_ORDER (
	ORDER_NUMBER NUMBER(10)			NOT NULL PRIMARY KEY,
	GROCERYSTORE_ID NUMBER(10)		NOT NULL,
	ORDER_DATE DATE,
	CONSTRAINT ORDER_GROCERYSTORE_ID_FK FOREIGN KEY (GROCERYSTORE_ID) REFERENCES GROCERY_STORE(GROCERYSTORE_ID));
  
CREATE TABLE ORDER_DETAIL (
	ORDER_NUMBER NUMBER(10)					NOT NULL,
	DETAIL_LINE_NUMBER NUMBER(10)				NOT NULL,
	CEREAL_PACK_ID NUMBER(10)				NOT NULL,
	PRIMARY KEY(ORDER_NUMBER,DETAIL_LINE_NUMBER),	
	QUANTITY_PER_ITEM NUMBER(10)				DEFAULT(0),
	NEGOTIATED_PRICE_PER_ITEM NUMBER(4,2)			DEFAULT(0.00),
	CONSTRAINT OD_ORDER_NUMBER_FK FOREIGN KEY (ORDER_NUMBER) REFERENCES PRODUCT_ORDER(ORDER_NUMBER),
	CONSTRAINT OD_CEREAL_PACK_ID_FK FOREIGN KEY (CEREAL_PACK_ID) REFERENCES CEREAL_BOX(CEREAL_PACK_ID));

  
