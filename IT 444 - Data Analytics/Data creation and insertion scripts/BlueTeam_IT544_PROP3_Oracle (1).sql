CREATE TABLE POSITION
(
 POSITION_NO			NUMBER(4) NOT NULL,
 POSITION_DESCRIPTION	VARCHAR2(50) NOT NULL,
 POSITION_SALARY      NUMBER(8) NOT NULL,
 CONSTRAINT POSITION_PK PRIMARY KEY (POSITION_NO)
);
/

CREATE TABLE BRANCH
(
 BRANCH_NO		NUMBER(4)	NOT NULL,
 BRANCH_STREET	VARCHAR2(50),
 BRANCH_CITY	VARCHAR2(20)	NOT NULL,
 BRANCH_STATE	VARCHAR2(2)	NOT NULL,
 BRANCH_ZIPCODE NUMBER(5),
 CONSTRAINT BRANCH_PK PRIMARY KEY (BRANCH_NO)
);
/

CREATE TABLE STAFF
(
 STAFF_NO		NUMBER(4)	NOT NULL,
 STAFF_FNAME	VARCHAR2(20) NOT NULL,
 STAFF_LNAME	VARCHAR2(20) NOT NULL,
 STAFF_SEX		VARCHAR2(1),
 STFF_DOB     DATE,
 POSITION_NO	NUMBER(4),
 BRANCH_NO		NUMBER(4),
 CONSTRAINT STAFF_PK	PRIMARY KEY (STAFF_NO),
 CONSTRAINT STAFF_POSITIONNO_FK FOREIGN KEY (POSITION_NO) REFERENCES POSITION(POSITION_NO),
 CONSTRAINT STAFF_BRANCHNO_FK FOREIGN KEY (BRANCH_NO) REFERENCES BRANCH(BRANCH_NO),
 CONSTRAINT STAFF_SEX_CHK CHECK (STAFF_SEX = 'M' OR STAFF_SEX = 'F')
);
/

CREATE TABLE SERVICE
(
 SERVICE_NO		NUMBER(4)	NOT NULL,
 SERVICE_TYPE	VARCHAR2(50) NOT NULL,
 CONSTRAINT SERVICE_PK	PRIMARY KEY (SERVICE_NO)
);
/

CREATE TABLE CONTRACTOR
(
 CONTRACTOR_NO			NUMBER(4) NOT NULL,
 CONTRACTOR_NAME		VARCHAR2(20) NOT NULL,
 CONTRACTOR_STREET		VARCHAR2(20),
 CONTRACTOR_CITY		VARCHAR2(20),
 CONTRACTOR_STATE		VARCHAR2(2),
 CONTRACTOR_ZIPCODE		NUMBER(5),
 CONTRACTOR_PHONENUM	NUMBER(10) NOT NULL,
 CONTRACTOR_EMAIL		VARCHAR2(30),
 CONSTRAINT CONTRACTOR_PK	PRIMARY KEY (CONTRACTOR_NO)
);
/

CREATE TABLE SERVICE_CONTRACTOR
(
 SERVICE_CONTRACTOR_NO	NUMBER(4) NOT NULL,
 CONTRACTOR_NO			NUMBER(4),
 SERVICE_NO				NUMBER(4),
 CONSTRAINT SC_PK	PRIMARY KEY (SERVICE_CONTRACTOR_NO),
 CONSTRAINT SC_CONTRACTORNO_FK FOREIGN KEY (CONTRACTOR_NO) REFERENCES CONTRACTOR (CONTRACTOR_NO),
 CONSTRAINT SC_SERVICENO_FK FOREIGN KEY (SERVICE_NO) REFERENCES SERVICE (SERVICE_NO)
);
/

CREATE TABLE REPAIR
(
 REPAIR_NO		NUMBER(4) NOT NULL,
 SERVICE_NO		NUMBER(4),
 ISREPAIR_PAID	VARCHAR2(1) DEFAULT ('N'),
 REPAIR_STATUS	VARCHAR2(12) DEFAULT ('INCOMPLETE'),
 REPAIR_TYPE	VARCHAR(10) DEFAULT ('NORMAL'),
 PROPERTY_NO	VARCHAR(5),
 CONSTRAINT	REPAIR_PK	PRIMARY KEY	(REPAIR_NO),
 --CONSTRAINT REPAIR_PROPERTYNO_FK FOREIGN KEY (PROPERTY_NO) REFERENCES PROPERTY(PROPERTY_NO),
 CONSTRAINT REPAIR_SERVICENO_FK FOREIGN KEY (SERVICE_NO) REFERENCES SERVICE(SERVICE_NO),
 CONSTRAINT REPAIR_ISREPAIRPAID_CHK CHECK(ISREPAIR_PAID = 'Y' OR ISREPAIR_PAID = 'N'),
 CONSTRAINT REPAIR_STATUS_CHK CHECK(REPAIR_STATUS = 'COMPLETE' OR REPAIR_STATUS = 'INCOMPLETE'),
 CONSTRAINT REPAIR_TYPE_CHK CHECK (REPAIR_TYPE = 'NORMAL' OR REPAIR_TYPE = 'EMERGENCY')
);
/

CREATE TABLE CONTRACT
(
 REPAIR_NO					NUMBER(4)	 NOT NULL,
 CONTRACTOR_NO				NUMBER(4) NOT NULL,
 RC_ESTIMATED_START_DATE	DATE		 NOT NULL,
 RC_ESTIMATED_END_DATE		DATE		 NOT NULL,
 RC_ACTUAL_START_DATE		DATE		 NOT NULL,
 RC_ACTUAL_END_DATE			DATE		 NOT NULL,
 RC_ESTIMATED_COST			NUMBER(8,2) NOT NULL,
 RC_ACTUAL_COST				NUMBER(8,2) NOT NULL,
 CONSTRAINT CONTRACT_PK	PRIMARY KEY (REPAIR_NO, CONTRACTOR_NO),
 CONSTRAINT CONRACT_REPAIRNO_FK FOREIGN KEY (REPAIR_NO) REFERENCES REPAIR(REPAIR_NO),
 CONSTRAINT CONRACT_CONTRACTORNO_FK FOREIGN KEY (CONTRACTOR_NO) REFERENCES CONTRACTOR(CONTRACTOR_NO),
 CONSTRAINT C_ESTIMATEDDATEVALID_CHK CHECK (RC_ESTIMATED_START_DATE < RC_ESTIMATED_END_DATE),
 CONSTRAINT C_ACTUALDATEVALID_CHK CHECK (RC_ACTUAL_START_DATE < RC_ACTUAL_END_DATE)
);
/

CREATE TABLE REPAIR_INQUIRY
(
 INQUIRYNO	NUMBER(10) NOT NULL,
